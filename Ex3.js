class Person {
  name = "";
  address = "";
  constructor(name, address) {
    this.name = name;
    this.address = address;
  }
}

class Student {
  name = "";
  address = "";
  program = "";
  year = 0;
  fee = 0;
  constructor(program, year, fee) {
    this.program = program;
    this.year = year;
    this.fee = fee;
  }
}

class Staff {
  name = "";
  address = "";
  school = "";
  pay = 0;
  constructor(school, pay) {
    this.school = school;
    this.pay = pay;
  }
}
